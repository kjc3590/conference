package com.fine.confernce;

public class FineVo {
	
	private int confe_id;
	private String confe_name;
	private String confe_affiliation;
	private String confe_position;
	private String confe_email;
	private String confe_phone;
	
	public int getConfe_id() {
		return confe_id;
	}
	public void setConfe_id(int confe_id) {
		this.confe_id = confe_id;
	}
	public String getConfe_name() {
		return confe_name;
	}
	public void setConfe_name(String confe_name) {
		this.confe_name = confe_name;
	}
	public String getConfe_affiliation() {
		return confe_affiliation;
	}
	public void setConfe_affiliation(String confe_affiliation) {
		this.confe_affiliation = confe_affiliation;
	}
	public String getConfe_position() {
		return confe_position;
	}
	public void setConfe_position(String confe_position) {
		this.confe_position = confe_position;
	}
	public String getConfe_email() {
		return confe_email;
	}
	public void setConfe_email(String confe_email) {
		this.confe_email = confe_email;
	}
	public String getConfe_phone() {
		return confe_phone;
	}
	public void setConfe_phone(String confe_phone) {
		this.confe_phone = confe_phone;
	}
	

}
